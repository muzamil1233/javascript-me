// Destructuring is the shorthand syntax that allow you to 
//  unpack the values from array or properties from  object into  individual  variables 

 const number = [1,2,3];
 const [a,b,c] = number;
 console.log(a); // 1
 console.log(b); // 2

 const person = {name : 'muzamil' , city:  'sopore'};
 const [name , city] = person;
 console.log(name) // muzamil 

 